package org.safs.data.model;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;
import javax.persistence.UniqueConstraint;

/**
 * @author Lei Wang
 *
 */
@Entity
@Table(uniqueConstraints={ @UniqueConstraint(columnNames = {"name", "version"}) })
public class Engine extends UpdatableDefault<Engine> implements RestModel{
	/** 'engines' the base path to access entity */
	public static final String REST_BASE_PATH = "engines";
	@Id
	@GeneratedValue(strategy=GenerationType.IDENTITY)
	private Long id;

	private String name;
	private String version;
	private String description;
	/**
	 *
	 */
	public Engine() {
		super();
	}
	/**
	 * @param name
	 * @param version
	 * @param description
	 */
	public Engine(String name, String version, String description) {
		super();
		this.name = name;
		this.version = version;
		this.description = description;
	}
	/**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the version
	 */
	public String getVersion() {
		return version;
	}
	/**
	 * @param version the version to set
	 */
	public void setVersion(String version) {
		this.version = version;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	@Override
	public String getRestPath() {
		return REST_BASE_PATH;
	}
}

